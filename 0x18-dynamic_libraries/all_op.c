#include <stdio.h>
/**
* add - add two numbers
* @a: first number
* @b: second number
* Return: Addition
*/
int add(int a, int b)
{
return(a + b);
}

/**
* sub - subtract two numbers
* @a: first number
* @b: second number
* Return: subtraction of two numbers
*/
int sub(int a, int b)
{
return(a - b);
}

/**
* div - divide two numbers
* @a: first number
* @b: second number
* Return: division
*/
int div(int a, int b)
{
return(a / b);
}

/**
* mod - modulus two numbers
* @a: first number
* @b: second number
* Return: modulus of two numbers
*/
int mod(int a, int b)
{
return(a % b);
}

/**
* mul - multiply two numbers
* @a: first number
* @b: second number
* Return: multiplication
*/
int mul(int a, int b)
{
return(a * b);
}
